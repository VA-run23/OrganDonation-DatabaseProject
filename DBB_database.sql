  intestine TINYINT(1) DEFAULT 0 CHECK (intestine IN (0, 1)),
  pancreas TINYINT(1) DEFAULT 0 CHECK (pancreas IN (0, 1)),
  FOREIGN KEY (uniqueID) REFERENCES donor_data(uniqueID) ON DELETE CASCADE 
